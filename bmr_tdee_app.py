def calculate_bmr(gender, weight, height, age):
    if gender == "‡∏ä‡∏≤‡∏¢":
        return 66 + (13.7 * weight) + (5 * height) - (6.8 * age)
    else:
        return 655 + (9.6 * weight) + (1.8 * height) - (4.7 * age)

